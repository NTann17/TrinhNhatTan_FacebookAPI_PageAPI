var builder = WebApplication.CreateBuilder(args);

// Add services to the container.

builder.Services.AddControllers();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();
builder.Services.Configure<FacebookPageApi.Configuration.FacebookOptions>(
    builder.Configuration.GetSection(FacebookPageApi.Configuration.FacebookOptions.SectionName));
builder.Services.AddHttpClient<FacebookPageApi.Services.IFacebookGraphService, FacebookPageApi.Services.FacebookGraphService>();

var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}

app.UseHttpsRedirection();

app.UseAuthorization();

app.MapControllers();

app.Run();
